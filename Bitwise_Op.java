public class Bitwise_Op {
    public static void main(StringMethods[] args) {
        int a=5;
        int b=7;

        // Bitwise AND(&).
        int c=a&b;
        System.out.println("A & B: "+c);

        // Bitwise OR(|).
        int d=a|b;
        System.out.println("A | B: "+d);

        int e=a^b;
        System.out.println("A ^ B: "+e);
    }
}
